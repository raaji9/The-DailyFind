const parseProducts = (productsText) => {
  const productList = [];
  const productStrings = productsText.split('\n\n');

  const categories = ["Electronics", "Books", "Clothing", "Home Appliances", "Daily Products"];
  const maxProductsPerCategory = 5;
  const productCounts = {};
  categories.forEach(category => productCounts[category] = 0);

  productStrings.forEach(productString => {
    if (productString.trim() === '') return;

    const lines = productString.split('\n');
    const name = lines[0];
    const description = lines[1];
    const category = lines[2];

    if (!categories.includes(category)) return;

    if (productCounts[category] >= maxProductsPerCategory) return;
    productCounts[category]++;

    let specifications = {};
    let imageUrl = "";
    try {
      const specificationsLine = lines[3];
      const specificationsArray = specificationsLine.substring(specificationsLine.indexOf(':') + 2).split(', ');
      specificationsArray.forEach(spec => {
        const parts = spec.split(': ');
        if (parts.length === 2) {
          specifications[parts[0].trim()] = parts[1].trim();
        }
      });
    } catch (e) {
      console.log("error parsing specifications", e)
    }

    switch (category) {
      case "Electronics":
        imageUrl = "/assets/Electronics.png";
        break;
      case "Daily Products":
        imageUrl = "/assets/Daily Products.png";
        break;
      case "Home Appliances":
        imageUrl = "/assets/Home Appliances.png";
        break;
      case "Clothing":
        imageUrl = "/assets/Clothing.png";
        break;
      case "Books":
        imageUrl = "/assets/Books.png";
        break;
      default:
        imageUrl = "https://via.placeholder.com/150";
    }

    productList.push({
      id: productList.length + 1,
      name: name,
      description: description,
      imageUrl: imageUrl,
      category: category,
      specifications: specifications,
    });
  });

  return productList;
};

const products = parseProducts(productsText);

export default products;
